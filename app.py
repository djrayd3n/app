# Prosty program do sumowania dwóch liczb

# Prosimy użytkownika o wpisanie pierwszej liczby
liczba1 = float(input("Wpisz pierwszą liczbę: "))

# Prosimy użytkownika o wpisanie drugiej liczby
liczba2 = float(input("Wpisz drugą liczbę: "))

# Sumujemy obie liczby
suma = liczba1 + liczba2

# Wyświetlamy wynik
print(f"Suma {liczba1} i {liczba2} wynosi: {suma}")
